    private void mostrarPokemon(int noMascota) {
        if (tienda.pokemones[noMascota].isVivo()) {
            LabelNombre.setText(tienda.getPokemones(noMascota).getNombre());
            LabelNivel.setText(tienda.getPokemones(noMascota).getNivelString());
            LabelTipo.setText(tienda.getPokemones(noMascota).getTipo());
            imagenPokemon.setIcon(new ImageIcon(tienda.getPokemones(noMascota).getImagen()));
            labelMostrarHambre.setText(tienda.getPokemones(noMascota).getHambreString());
            labelEfecto.setText(tienda.getPokemones(noMascota).getEfectoComida());
            labelEnfermedades.setText(tienda.getPokemones(noMascota).getEnfermedadString());
            labelLimpiar.setText(tienda.getPokemones(noMascota).getLimpiar());
            labelPasear.setText(tienda.getPokemones(noMascota).getPasear());
            this.posicionActual = noMascota;
        } else {
            imagenPokemon.setIcon(new ImageIcon("C:/Users/julio/Documents/Tareas universidad tercer semestre/intro a la progra/Practica2/src/main/java/com/mycompany/practica2/imagenes/muerto.png"));
        }
    }

