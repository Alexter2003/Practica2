    private void BotonRevivirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRevivirActionPerformed
        try {
            int pokemonARevivir = Integer.parseInt(labelAnimalARevivir.getText());
            if (!tienda.getPokemones(pokemonARevivir).isVivo()) {
                int precio = (5 * tienda.getPokemones(pokemonARevivir).getNivel()) + 10;
                tienda.setDinero(precio);
                tienda.getPokemones(pokemonARevivir).cambiarEstado(true);
                JOptionPane.showMessageDialog(this, tienda.getPokemones(pokemonARevivir).getNombre() + " ha revivido");
                tienda.getPokemones(pokemonARevivir).resume();
                tienda.getPokemones(pokemonARevivir).reiniciarHambre();
            } else {
                JOptionPane.showMessageDialog(this, "Este pokemon esta vivo");
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "No hay ningun pokemon en esta posicion");
        }
    }//GEN-LAST:event_BotonRevivirActionPerformed

